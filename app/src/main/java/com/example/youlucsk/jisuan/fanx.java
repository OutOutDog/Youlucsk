package com.example.youlucsk.jisuan;

import java.util.ArrayList;
import java.util.List;

public class fanx {

    List<Integer> ljs = new ArrayList<>();
	int cd;
	
	
	
	public fanx(List<Integer> ljs, int cd) {
		super();
		this.ljs = ljs;
		this.cd = cd;
	}
	public List<Integer> getLjs() {
		return ljs;
	}
	public void setLjs(List<Integer> ljs) {
		this.ljs = ljs;
	}
	public int getCd() {
		return cd;
	}
	public void setCd(int cd) {
		this.cd = cd;
	}
	
	
	

	
}
